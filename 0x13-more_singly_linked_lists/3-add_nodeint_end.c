#include "lists.h"

/**
 * add_nodeint_end - adds a node at the tail of the linkedlist
 * @head: pointer to linkedlist head
 * @n: value to add to new node
 * Return: updated linkedlist or NULL if the operation fails
 */
listint_t *add_nodeint_end(listint_t **head, const int n)
{
	listint_t *new;
	listint_t *temp = *head;

	new = malloc(sizeof(listint_t));
	if (new == NULL)
		return (NULL);

	new->n = n;
	new->next = NULL;

	if (*head == NULL)
	{
		*head = new;
	}
	else
	{
		while (temp->next != NULL)
			temp = temp->next;
		temp->next = new;
	}
	return (*head);
}
