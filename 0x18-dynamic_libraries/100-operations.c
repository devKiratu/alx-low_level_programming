/**
 * add - adds two integers @a and @b
 * @a: first integer
 * @b: second integer
 * Return: sum of the two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts @b from @a
 * @a: first integer
 * @b: second integer
 * Return: result of @a - @b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers @a and @b
 * @a: first integer
 * @b: second integer
 * Return: product of @a and @b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides @a by @b
 * @a: dividend
 * @b: divisor
 * Return: result of @a / @b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates the remainder of @a % @b
 * @a: first integer
 * @b: second integer
 * Return: result of @a % @b
 */
int mod(int a, int b)
{
	return (a % b);
}
